const fs = require("node:fs");
const path = require("node:path");

const root = path.resolve(__dirname, "..");
const sourceFiles = [
  "src/config.js",
  "src/utils.js",
  "src/data.js",
  "src/providers.js",
  "src/ui.js",
  "src/main.js",
];

const js = sourceFiles
  .map((file) => fs.readFileSync(path.join(root, file), "utf8"))
  .join("\n\n");

fs.writeFileSync(
  path.join(root, "app.js"),
  `// Generated by tools/build-apps-script-index.js. Edit files in src/ instead.\n${js}\n`
);

const html = fs.readFileSync(path.join(root, "index.html"), "utf8");
const css = fs.readFileSync(path.join(root, "styles.css"), "utf8");
const builtJs = fs.readFileSync(path.join(root, "app.js"), "utf8");

const bundled = html
  .replace('<link rel="stylesheet" href="./styles.css">', `<style>\n${css}\n</style>`)
  .replace('<script src="./app.js" defer></script>', `<script>\n${builtJs}\n</script>`);

fs.mkdirSync(path.join(root, "google-apps-script"), { recursive: true });
fs.writeFileSync(
  path.join(root, "google-apps-script", "Index.html"),
  `<!-- Generated by tools/build-apps-script-index.js. Do not edit this file by hand. -->\n${bundled}`
);

console.log("Wrote app.js and google-apps-script/Index.html");
